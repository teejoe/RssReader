package org.m2x.rssreader;

import android.app.Application;
import android.content.Context;

/**
 * The Application object of the APP, which can get the application 
 * Context by a static method. It must be declared in the AndroidManifest.xml 
 * for Application:name attribute.
 * @author teejoe(mtjm2x@gmail.com)
 *
 */
public class MainApplication extends Application {

    private static Context context; // The application context.

    @Override
    public void onCreate() {
        super.onCreate();
        context = getApplicationContext();
    }

    public static Context getContext() {
        return context;
    }
}